    private void jButtonRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistroActionPerformed
        try {
            conn = conexion.conectar();
            String sql = "Select*FROM administrador where Ad_Id >= 1 ";
            sent = conn.createStatement();
            ResultSet rs = sent.executeQuery(sql);
            if (rs.last()) {
                try {
                    String sqql = "Select * FROM administrador where Ad_Nip=?";
                    int nip = Integer.parseInt(JOptionPane.showInputDialog(null, "Introduce NIP"));
                    pst = conn.prepareStatement(sqql);
                    pst.setInt(1, nip);
                    rs = pst.executeQuery();
                    if (rs.next()) {
                        Registro re = new Registro();
                        re.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(null, "NIP incorreto");
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Introduce solo números");
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(null, "Ocurrio una falla contacte a su proveedor.", "Falla", JOptionPane.ERROR_MESSAGE);
                    System.out.println("Error ->" + e.getMessage());
                }
            } else {
                Registro re = new Registro();
                re.setVisible(true);
                this.setVisible(false);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio una falla contacte a su proveedor.", "Falla", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error ->" + e.getMessage());
        }

    }//GEN-LAST:event_jButtonRegistroActionPerformed

