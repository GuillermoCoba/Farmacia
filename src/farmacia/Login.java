    public void acceso() {
        conn = conexion.conectar();
        String Sql = "Select * from administrador where Ad_Usuario=? and Ad_Contraseña=? ";
        String sqql = "Select * from empleados where Em_Usuario=? and Em_Contraseña=? ";
        try {
            pst = conn.prepareStatement(Sql);
            psst = conn.prepareStatement(sqql);
            pst.setString(1, jTextUsuario.getText());
            pst.setString(2, jPasswordField1.getText());
            psst.setString(1, jTextUsuario.getText());
            psst.setString(2, jPasswordField1.getText());
            rs = pst.executeQuery();
            rss = psst.executeQuery();
            if (rs.next()) {
                String adm = "Bienvenido Administrador";
                JOptionPane.showMessageDialog(null, adm);
                Inicio in = new Inicio();
                in.setVisible(true);
                this.setVisible(false);
            } else {
                if (rss.next()) {
                    String emp = "Bienvenido Empleado";
                    JOptionPane.showMessageDialog(null, emp);
                    InicioE inn = new InicioE();
                    inn.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "User o password invalido", "Acesso denegago", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio una falla contacte a su proveedor.", "Falla", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error ->" + e.getMessage());
        }
    }

