    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        try {
            int fila = jTable1.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un empleado.");
            } else {
                String sql = "delete from empleados where Em_Id= " + jTable1.getValueAt(fila, 0);
                sent = conn.createStatement();
                int n = sent.executeUpdate(sql);
                if (n > 0) {
                    LlenarTabla();
                    JOptionPane.showMessageDialog(null, "Empleado Eliminado");
                }
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null,"Ocurrio una falla contacte a su proveedor.", "Falla", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

