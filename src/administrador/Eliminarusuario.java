    void LlenarTabla() {
        try {
            conn = conexion.conectar();
            String[] titulos = {"IdEmpleado", "Nombre", "Apellidos", "Usuario", "Contraseña", "Correo"};
            String sql = "Select * from empleados";
            model = new DefaultTableModel(null, titulos);
            sent = conn.createStatement();
            ResultSet rs = sent.executeQuery(sql);
            String[] fila = new String[6];
            while (rs.next()) {
                fila[0] = rs.getString("Em_Id");
                fila[1] = rs.getString("Em_Nombre");
                fila[2] = rs.getString("Em_Apellidos");
                fila[3] = rs.getString("Em_Usuario");
                fila[4] = rs.getString("Em_Contraseña");
                fila[5] = rs.getString("Em_Correo");
                model.addRow(fila);
            }
            jTable1.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio una falla contacte a su proveedor.", "Falla", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error ->" + e.getMessage());
        }
    }

