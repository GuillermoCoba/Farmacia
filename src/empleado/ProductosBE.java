    private void cargarlistaproductos(String dato) {
        try {
            conn = conexion.conectar();
            //String[] Titulo = {"Codigo", "Producto", "Presentación", "Precio", "Stock"};
            //tabla = new DefaultTableModel(null, Titulo);
            tabla = (DefaultTableModel) tbprod.getModel();
            tabla.setRowCount(0);
            String[] Registro = new String[5];
            String mostrar = "SELECT * FROM productos WHERE CONCAT (Pr_Id,'',Pr_Nombre,'',Pr_Presentacion) LIKE '%" + dato + "%'";
            sent = conn.createStatement();
            rs = sent.executeQuery(mostrar);
            while (rs.next()) {
                Registro[0] = rs.getString("Pr_Id");
                Registro[1] = rs.getString("Pr_Nombre");
                Registro[2] = rs.getString("Pr_Presentacion");
                Registro[3] = rs.getString("Pr_Precio");
                Registro[4] = rs.getString("Pr_Cantidad");
                tabla.addRow(Registro);
            }
            tbprod.setModel(tabla);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Ocurrio una falla contacte a su proveedor.", "Falla", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error ->" + e.getMessage());
        }
    }

