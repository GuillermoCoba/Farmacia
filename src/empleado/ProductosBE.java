    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel tabladet = (DefaultTableModel) VentasE.jTable1.getModel();
            String[] dato = new String[6];

            int fila = tbprod.getSelectedRow();

            if (fila == -1) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar un producto.");
            } else {
                String codins = tbprod.getValueAt(fila, 0).toString();
                String desins = tbprod.getValueAt(fila, 1).toString();
                String presins = tbprod.getValueAt(fila, 2).toString();
                String precins = tbprod.getValueAt(fila, 3).toString();

                int mismoProd = 0;

                String cant = JOptionPane.showInputDialog("Ingrese la cantidad deseada del producto.");
                if ((cant.equals("")) || (cant.equals("0"))) {
                    JOptionPane.showMessageDialog(null, "<html>Debe ingresar una cantidad mayor a 0. Intente nuevamente.<br><b>Si desea eliminar un producto utilice el botón Eliminar en la ventana de ventas.</b></html>");
                } else {
                    int canting = Integer.parseInt(cant);
                    if (canting <= 0) {
                        JOptionPane.showMessageDialog(null, "Debe ingresar una cantidad mayor a 0.\nIntente nuevamente.");
                    } else {
                        int comp = Integer.parseInt(Consulta.comparar(codins));
                        if (canting > comp) {
                            JOptionPane.showMessageDialog(null, "No hay suficiente stock del producto.\nIntente nuevamente.");
                        } else {
                            for (int i = 0; i < VentasE.jTable1.getRowCount(); i++) {
                                String com = VentasE.jTable1.getValueAt(i, 0).toString();
                                if (codins.equals(com)) {
                                    tabladet.setValueAt(cant, i, 4);
                                    mismoProd = ++mismoProd;
                                }
                            }
                            if (mismoProd == 0) {

                                dato[0] = codins;
                                dato[1] = desins;
                                dato[2] = presins;
                                dato[3] = precins;
                                dato[4] = cant;
                                tabladet.addRow(dato);
                                VentasE.jTable1.setModel(tabladet);
                                VentasE.calcular();
                            }
                            DefaultTableModel model = (DefaultTableModel) tbprod.getModel();
                            model.setValueAt(comp - canting, fila, 4);
                        }
                    }
                }
            }
        } catch (NullPointerException e) {
            System.out.println("error por cancelación. - " + e.getMessage());
        } catch (HeadlessException | NumberFormatException e) {
            System.out.println("error por cancelación alert. - " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Ingrese solo números.\nIntente nuevamente.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

